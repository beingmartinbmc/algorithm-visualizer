import type { Airport, FlightRoute, TravelScenario } from '../types/worldMap';

function project(lon: number, lat: number) {
  return {
    x: ((lon + 180) / 360) * 1000,
    y: ((90 - lat) / 180) * 520,
  };
}

function airport(
  code: string,
  city: string,
  country: string,
  region: string,
  lat: number,
  lon: number,
  hubScore: number,
): Airport {
  return { code, city, country, region, lat, lon, hubScore, ...project(lon, lat) };
}

