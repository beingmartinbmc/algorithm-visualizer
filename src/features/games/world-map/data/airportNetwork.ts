const airportByCode = new Map(AIRPORTS.map((item) => [item.code, item]));

function haversineKm(a: Airport, b: Airport): number {
  const earthRadiusKm = 6371;
  const toRad = (value: number) => (value * Math.PI) / 180;
  const dLat = toRad(b.lat - a.lat);
  const dLon = toRad(b.lon - a.lon);
  const lat1 = toRad(a.lat);
  const lat2 = toRad(b.lat);
  const h = Math.sin(dLat / 2) ** 2 + Math.cos(lat1) * Math.cos(lat2) * Math.sin(dLon / 2) ** 2;
  return Math.round(2 * earthRadiusKm * Math.asin(Math.sqrt(h)));
}

function route(source: string, destination: string): FlightRoute {
  const a = airportByCode.get(source);
  const b = airportByCode.get(destination);
  if (!a || !b) throw new Error(`Unknown airport route: ${source}-${destination}`);
  const distanceKm = haversineKm(a, b);
  return {
    id: `${source}-${destination}`,
    source,
    destination,
    distanceKm,
    durationHours: Math.round((distanceKm / 870 + 0.65) * 10) / 10,
    costUsd: Math.round(distanceKm * 0.11 + 140),
  };
}

